//! Destructor:-
// Destructor is a special member function of a class which is automatically called when an object of that class is destroyed
// Destructor is used to release the resources which are acquired by the class

//! The major points about Destructor:-
// 1. Destructor is a special member function of a class which is automatically called when an object of that class is destroyed.
// 2. Destructor is used to release the resources which are acquired by the class.
// 3. Destructor is a member function of a class which has a prefix ~ (Tilde is used to refer to the destructor function of the class itself)
// 4. Destructor does not take any parameters and does not return any value.
// 5. Destructor can be overloaded.
// 6. Destructor can be declared as static.
// 7. Destructor can be declared as virtual.
// 8. Destructor can be declared as pure virtual.
// 9. Destructor can be declared as friend.