//! Operator Overloading

// Operator Overloading is one of the most important feature of C++ which is a type of Polymorphism.

// Using the concept of Operator Overloading, we can overload any built-in operator i.e we can assign a new definition of existing operators.

//! Every Operator can be Overload except these below:-

// 1. Assignment operator (=)
// 2. Member access operator (.)
// 3. Scope resolution operator (::)
// 4. sizeof operator (sizeof)
// 5. typeid operator (typeid)
// 6. Comprehension operator (?:)
// 7. Comma operator (,)
// 8. Astric operator (*)